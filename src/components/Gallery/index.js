class Gallery extends Component {
  state = {
    imgId: imagesList[0].id,
  }

  onSelectImg = uniqueId => {
    this.setState({
      imgId: uniqueId,
    })
  }

  render() {
    const {imgId} = this.state
    const selectedImg = imagesList.filter(eachImg => eachImg.id === imgId)

    const {imageUrl, imageAltText} = selectedImg[0]

    return (
      <div className="bg_container">
        <div className="img_container">
          <img src={imageUrl} alt={imageAltText} className="img" />
          <h1 className="hading">Nature Photography</h1>
          <p className="para">Nature Photography by Rahul</p>
          <ul className="ul_list">
            {imagesList.map(imageDetails => (
              <ThumbnailItem
                key={imageDetails.id}
                imageDetails={imageDetails}
                onSelectImg={this.onSelectImg}
                isActive={imgId === imageDetails.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
